package ec.com.control.acceso.exception;

public class RolAplicacionException extends Exception {

	private static final long serialVersionUID = -7280120866853321637L;

	public RolAplicacionException() {
		super();
	}

	public RolAplicacionException(String message, Throwable cause) {
		super(message, cause);
	}

	public RolAplicacionException(String message) {
		super(message);
	}

	public RolAplicacionException(Throwable cause) {
		super(cause);
	}
	
}
