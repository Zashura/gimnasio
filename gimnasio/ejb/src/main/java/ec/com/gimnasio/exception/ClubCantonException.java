package ec.com.gimnasio.exception;

public class ClubCantonException extends Exception {

	private static final long serialVersionUID = -7280120866853321637L;

	public ClubCantonException() {
		super();
	}

	public ClubCantonException(String message, Throwable cause) {
		super(message, cause);
	}

	public ClubCantonException(String message) {
		super(message);
	}

	public ClubCantonException(Throwable cause) {
		super(cause);
	}
	
}
